function howMany(array){
  const total = {};
  for(let i in array){
    let value = array[i];
    if (!total[value]) {
      total[value] = 0;
    } else {
      total[value] += 1;
    }
  }
  const messages = [];
  for (value in total){
    let message = ``;
    if (total[value] = 1){
      message = `${total[value]} ${value}`;
      } else {
      message = `${total[value]} ${value} + 's'}`;
      }
    messages.push(message);
  }
  return console.log(`Sua cesta possui: ${messages.join(', ')}.`)
}

