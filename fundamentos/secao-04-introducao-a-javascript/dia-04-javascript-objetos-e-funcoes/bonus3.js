function sortBasket (arr) {
  let message = 'Sua cesta possui: ';
  const resultObject = {};
  let quantity = 1;
  for (let i = 0; i < (arr.length -1); i += 1) {
    let currentItem = arr[i];
    for (let r = 1; r < arr.length; r += 1) {
      if (currentItem === arr[r]) {
        quantity += 1;
        resultObject[currentItem] = quantity;
      }
    }
    arr.splice(i, 0);
    quantity = 0;
  }
  resultObject[arr[0]] += 1;
  const keys = Object.keys(resultObject);
  for (let k = 0; k < keys.length; k += 1) {
    let item = keys[k];
    let itemQnt = resultObject[item];
    if ( itemQnt > 1) {
      let itemPlural = item + 's';
      item = itemPlural;
    }
    if (k === (keys.length -1)) {
      message += itemQnt + ' ' + item + '.';
    } else {
      message += itemQnt + ' ' + item + ', ';
    }
  }
  return message;
}

